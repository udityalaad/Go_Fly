    public int GeneratePrice(){
        conn = AirlineReservationSystem.connectDB();
        
        try{
            int currPrice = 0;
            
            String sql1 = "Select * from FLIGHT where FLIGHT_ID=?";
            
            pst1 = (OraclePreparedStatement) conn.prepareStatement(sql1);
            pst1.setString(1, Integer.toString(AirlineReservationSystem.flight_id));
            rs = (OracleResultSet) pst1.executeQuery();
            
            if(rs.next()){
                if(AirlineReservationSystem.CLASS == "Economy"){
                    currPrice = Integer.parseInt(rs.getString("ECONOMY_PRICE"));
                }
                else if(AirlineReservationSystem.CLASS == "Business"){
                    currPrice = Integer.parseInt(rs.getString("BUSINESS_PRICE"));
                }
                
                if(Integer.parseInt(AGE.getText()) < 12){
                    currPrice = currPrice * 75 / 100;
                }
                
                return currPrice;
            }
            else{
                JOptionPane.showMessageDialog(null, "There was a problem generating the prices.");
                
                return 0;
            }
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
        return 0;
    }
    
    
