    private void ADD_PASSENGERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADD_PASSENGERActionPerformed
        // TODO add your handling code here:
        if(AirlineReservationSystem.passenger_count >= 5){
            JOptionPane.showMessageDialog(null,"Maximum 5 Passengers can be added at a time.");
            return;
        }
        
        
        conn = AirlineReservationSystem.connectDB();
        
        try{
            int count = 0, ticket_id = 0, currPrice = (int) GeneratePrice();
            
            SimpleDateFormat formatter = new SimpleDateFormat("dd-MMM-yyyy");
            
            String sql1 = "Select * from TICKET";
            
            pst1 = (OraclePreparedStatement) conn.prepareStatement(sql1);
            rs = (OracleResultSet) pst1.executeQuery();
            
            while(rs.next()){
                count++;
            }
            
            ticket_id = 100000 + (count + 1);
            
            
            String sql2 = "Insert into TICKET values (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

            pst2 = (OraclePreparedStatement) conn.prepareStatement(sql2);
            
            pst2.setString(1, Integer.toString(ticket_id));
            pst2.setString(2, Integer.toString(AirlineReservationSystem.flight_id));
            pst2.setString(3, formatter.format(AirlineReservationSystem.DOJ));
            pst2.setString(4, AirlineReservationSystem.CLASS);
            
            pst2.setString(5, AirlineReservationSystem.email);

            pst2.setString(6, FIRST_NAME.getText());
            pst2.setString(7, MIDDLE_NAME.getText());
            pst2.setString(8, LAST_NAME.getText());

            if(MALE.isSelected()){
                pst2.setString(9, "Male");
            }
            else if(FEMALE.isSelected()){
                pst2.setString(9, "Female");
            }
            else if(OTHER.isSelected()){
                pst2.setString(9, "Other");
            }

            pst2.setString(10, AGE.getText());
            
            if(MOBILE_NUMBER.getText().length() != 10){
                JOptionPane.showMessageDialog(null, "Invalid Mobile Number.");
                return;
            }
            else{
                pst2.setString(11, MOBILE_NUMBER.getText());
            }
            
            pst2.setString(12, Integer.toString(currPrice));
            
            pst2.setString(13, "Booked");
            
            result = pst2.executeUpdate();
            
            if(result != 0){
                JOptionPane.showMessageDialog(null,"Passeger Added Successfully.");
                AirlineReservationSystem.passenger_count++;
                
                
                FIRST_NAME.setText(null);
                MIDDLE_NAME.setText(null);
                LAST_NAME.setText(null);
                
                AGE.setText(null);
                MOBILE_NUMBER.setText(null);
                
                CURRENT_PASSENGERS_PRICE.setText(null);
                TOTAL_PRICE.setText(null);
                
            }
            else{
                JOptionPane.showMessageDialog(null,"There was a problem adding the passenger.");
            }
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }                                              

